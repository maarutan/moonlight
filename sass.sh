#!/usr/bin/env bash
# в”Ңв”Җв”җв”Ңв”Җв”җв”Ңв”Җв”җв”Ңв”Җв”җ    в”Ңв”Җв”җв”Ңв”Җв”җв”Ңв”¬в”җв”Ңв”Җв”җв”¬в”¬  в”Ңв”Җв”җв”¬в”Җв”җ
# в””в”Җв”җв”ңв”Җв”Өв””в”Җв”җв””в”Җв”җ    в”Ӯ  в”Ӯ в”Ӯв”Ӯв”Ӯв”Ӯв”ңв”Җв”ҳв”Ӯв”Ӯ  в”ңв”Ө в”ңв”¬в”ҳ
# в””в”Җв”ҳв”ҙ в”ҙв””в”Җв”ҳв””в”Җв”ҳв”Җв”Җв”Җв”Җв””в”Җв”ҳв””в”Җв”ҳв”ҙ в”ҙв”ҙ  в”ҙв”ҙв”Җв”ҳв””в”Җв”ҳв”ҙв””в”Җ
